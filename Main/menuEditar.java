    public puestoEst encontrarPuesto(Persona persona){ //Para conseguir el puesto que esta siendo usada por la persona
        for(int i=0; i<5; i++){
            for(int j=0; j<500; j++){
                if(Bienvenido.sectoresCarros[i][j].getOcupantes().existeOcupante(persona)){
                    return Bienvenido.sectoresCarros[i][j];
                }
            }
        }
        
        for(int i=0; i<2; i++){
            for(int j=0; j<100; j++){
                if(Bienvenido.sectoresMotos[i][j].getOcupantes().existeOcupante(persona)){
                    return Bienvenido.sectoresMotos[i][j];
                }
            }
        }
        return null;
    }

