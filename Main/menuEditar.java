    private void jButtonConfirmarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarCActionPerformed
        boolean Personavalido = false;
        boolean puestoValido = false;
        if(jTextFieldNombre.getText().matches("^[a-zA-Z]+$") && jTextFieldApellido.getText().matches("^[a-zA-Z]+$") && jTextFieldCI.getText().matches("^[0-9]+$")){
            Personavalido = true;
            jTextFieldNombre.setForeground(Color.black);
            jTextFieldNombre.setForeground(Color.black);
            jTextFieldCI.setForeground(Color.black);
            jTextUnidades.setForeground(Color.black);
            p.setNombre(jTextFieldNombre.getText());
            p.setApellido(jTextFieldApellido.getText());
            p.setTci(jComboBoxTCI.getSelectedItem().toString());
            p.setCi(jTextFieldCI.getText());
            p.setDEntrada(jComboDia.getSelectedItem().toString());
            p.setHEntrada(Integer.parseInt(jComboHEntrada.getSelectedItem().toString()));
            p.setMEntrada(Integer.parseInt(jComboMEntrada.getSelectedItem().toString()));
            p.setHSalida(Integer.parseInt(jComboHSalida.getSelectedItem().toString()));
            p.setMSalida(Integer.parseInt(jComboMSalida.getSelectedItem().toString()));
            if(!p.getCompras().esVacia()){
                if(!(jTextUnidades.getText().matches("^[0-9]+$") && Integer.parseInt(jTextUnidades.getText())!=0)){
                    Personavalido = false;
                    jTextUnidades.setForeground(Color.red);
                } else{
                    jTextUnidades.setForeground(Color.black);
                    Compra compraNueva = compras[jComboCompra.getSelectedIndex()];
                    p.getCompras().eliminarCompra(compraNueva);
                    compraNueva.setTienda(Bienvenido.tiendas.buscarTienda(jComboTiendas.getSelectedIndex()+1));
                    compraNueva.setProducto(compraNueva.getTienda().getProductos().buscarProducto(jComboProductos.getSelectedItem().toString()));
                    compraNueva.setUnidades(Integer.parseInt(jTextUnidades.getText()));
                    compraNueva.setPrecioTotal(Integer.parseInt(jTextUnidades.getText())*compraNueva.getProducto().getPrecio());
                    if(jRadioButtonOnline.isSelected()){
                        compraNueva.setTipoCompra("Online");
                    } else{
                        compraNueva.setTipoCompra("Presencial");
                    }
                    compraNueva.setMetodoPago(jComboMetodoPago.getSelectedItem().toString());
                    p.getCompras().agregarCompraAlFinal(compraNueva);
                }
            }
        } else{
            if(!jTextFieldNombre.getText().matches("^[a-zA-Z]+$")){
                jTextFieldNombre.setForeground(Color.red);
            } else{
                jTextFieldNombre.setForeground(Color.black);
            }
            if(!jTextFieldApellido.getText().matches("^[a-zA-Z]+$")){
                jTextFieldApellido.setForeground(Color.red);
            } else{
                jTextFieldApellido.setForeground(Color.black);
            }
            if(!jTextFieldCI.getText().matches("^[0-9]+$")){
                jTextFieldCI.setForeground(Color.red);
            } else{
                jTextFieldCI.setForeground(Color.black);
            }
            if(!jTextUnidades.getText().matches("^[0-9]+$") && Integer.parseInt(jTextUnidades.getText())==0){
                jTextUnidades.setForeground(Color.red);
            } else{
                jTextUnidades.setForeground(Color.black);
            }
        }
        if(Personavalido){
            puestoEst puestoNuevo = null;
            if(jCheckBoxVehiculo.isSelected()){
                if(jTextFieldPlaca.getText().matches("^[A-Z0-9]*$")){
                    jTextFieldPlaca.setForeground(Color.black);
                    p.setCarro(new Vehiculo(p, jComboTVehiculo.getSelectedItem().toString(), jTextFieldPlaca.getText(), jComboColor.getSelectedItem().toString(), jComboMarca.getSelectedItem().toString()));
                    puestoEst puestoCopia = puesto;
                    if(puesto!=null){
                        puesto.getOcupantes().eliminarPersona(p);
                    }
                    switch (jComboSector.getSelectedItem().toString()){
                        case "P":
                            puestoNuevo = Bienvenido.sectoresMotos[0][jComboNPuesto.getSelectedIndex()];
                            break;
                        case "M":
                            puestoNuevo = Bienvenido.sectoresCarros[0][jComboNPuesto.getSelectedIndex()];
                            break;
                        case "S":
                            puestoNuevo = Bienvenido.sectoresCarros[1][jComboNPuesto.getSelectedIndex()];
                            break;
                        case "R":
                            puestoNuevo = Bienvenido.sectoresCarros[2][jComboNPuesto.getSelectedIndex()];
                            break;
                        case "G":
                            puestoNuevo = Bienvenido.sectoresCarros[3][jComboNPuesto.getSelectedIndex()];
                            break;
                        case "B1":
                            puestoNuevo = Bienvenido.sectoresCarros[4][jComboNPuesto.getSelectedIndex()];
                            break;
                        case "B2":
                            puestoNuevo = Bienvenido.sectoresMotos[1][jComboNPuesto.getSelectedIndex()];
                            break;
                    }
                    if(puestoNuevo.getOcupantes().estaOcupado(p)){
                        puesto = puestoCopia;
                        jPanelOcupado.setVisible(true);
                    } else{
                        puestoNuevo.getOcupantes().agregarPersonaAlFinal(p);
                        jPanelOcupado.setVisible(false);
                        switch (jComboSector.getSelectedItem().toString()){
                            case "P":
                                Bienvenido.sectoresMotos[0][jComboNPuesto.getSelectedIndex()] = puestoNuevo;
                                break;
                            case "M":
                                Bienvenido.sectoresCarros[0][jComboNPuesto.getSelectedIndex()] = puestoNuevo;
                                break;
                            case "S":
                                Bienvenido.sectoresCarros[1][jComboNPuesto.getSelectedIndex()] = puestoNuevo;
                                break;
                            case "R":
                                Bienvenido.sectoresCarros[2][jComboNPuesto.getSelectedIndex()] = puestoNuevo;
                                break;
                            case "G":
                                Bienvenido.sectoresCarros[3][jComboNPuesto.getSelectedIndex()] = puestoNuevo;
                                break;
                            case "B1":
                                Bienvenido.sectoresCarros[4][jComboNPuesto.getSelectedIndex()] = puestoNuevo;
                                break;
                            case "B2":
                                Bienvenido.sectoresMotos[1][jComboNPuesto.getSelectedIndex()] = puestoNuevo;
                                break;
                        }
                        puestoValido = true;
                    }
                } else{
                   jTextFieldPlaca.setForeground(Color.red);
                }
            } else{
                p.setCarro(null);
                puestoValido = true;
            }
        }
        if(Personavalido && puestoValido){
            Bienvenido.personas.eliminarPersona(p);
            Bienvenido.personas.insertarPersona(p);
            try {
                Bienvenido B = new Bienvenido();
                B.setVisible(true);
                dispose();
            } catch (IOException ex) {
                Logger.getLogger(menuEditar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonConfirmarCActionPerformed

